<div id="tech"><a href="#tech"><h3 class="center">Tech</h3></a></div><br>
The goal is to have the game run smoothly on as many devices as possible, regardless of hardware and operating system, by keeping it lightweight and cross-platform. It's designed to work right from any modern web-browser, <a href="http://www.google.com/chrome/" target="_blank"><b>Google Chrome</b></a> being the recommended choice. Ancient Beast is developed using only native languages and web technologies, totally avoiding the need of installing any binaries or plug-ins. Also, every single asset of the website and of the game itself is carefully optimized in order to save time, bandwidth, disk space and memory.
